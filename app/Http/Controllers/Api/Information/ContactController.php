    {
        $result = Contact::create([
            'name' => $request->name,
            'email' => $request->email,
            'interest' => $request->interest,
            'contact' => $request->contact,
        ]);

        if ($result){
            return success_data('反馈成功');
        }else{
            return error_data('反馈失败');
        }
    }

